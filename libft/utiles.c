#include "libft.h"

int	all_space(char *str)
{
	int	i;

	i = 0;
	while (ft_isspace(str[i]) && str[i])
	{
		i++;
	}
	if (str[i] == '\0')
	{
		return (0);
	}
	return (1);
}
